			<h4>Light up the Interior</h4>
			<p>Lighting is one of the most important aspects of photography. Lights show the color, contrast, and depth of a setting in its most natural form. If your lighting is great, your photos will turn out awesome, and this helps to create an impression that you are a professional host worth staying with.</p>
			<p>It’s best to shoot during the day, letting in as much natural light as possible and turning the lights on. You don’t have to shoot in the direction of the windows because most modern cameras can adjust their lighting accordingly.You don’t want to create the impression that you are hiding something. Keep the blinds open to allow natural lighting flood the interior so they can see everywhere clearly.</p>
			<h4>Keep the space clean</h4><br>
			<p>Before you start shooting in every area of your unit, it’s important that you tidy up the space to make it free of clutter. This will make your place more welcoming, spacious and appeal to the traveler. Dust surfaces, arrange some magazines and place a flower vase on the table in the living room. Try to make your space squeaky clean, but ensure that the photos are not an exaggerated representation of the way things actually are on the ground, so travelers who booked your space don’t get disappointed.</p>
			<h4>The corner shot is best</h4><br>
			<p>Shooting in a corner accentuates the dimensions of the space more than taking pictures of a wall. Shooting at a wall may reduce the actual size of the space in reality, whereas, corners show a true representation of dimensionality.</p>
			<p>Also, make sure your photos are well composed by using the visual cues and the rule of thirds for balance to deliver a mesmerizing experience the viewer can connect to instantly.</p>
			<h4>Highlight unique features of your space</h4><br>
			<p>Does your kitchen really rock? An awe-inspiring view of a majestic river? A fireplace? Near the medical Center? These seemingly ordinary amenities make you stand out from places lacking them. Show them with gusto in your photos.</p>
			<p>The little things that provide extra comfort and fun to healthcare travelers, so don’t hesitate to show your home’s exciting features. It may be a memento from the 16th century or a disk plate autographed by Elvis Presley. Always include them in your pictures, because you never know what might sway somebody to book your space.</p>
			<p>Although we encourage you to take pictures of unique things, please keep in mind things of great value to the host we don’t encourage to leave inside the listing due to possible damages or being misplaced.</p>
			<h4>Take pictures of the exterior</h4><br>
			<p>The interior of your space should be given as much importance as the exterior. Help people envision arriving at your place by providing outdoor shots.</p>
