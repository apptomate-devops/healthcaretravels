                        <h1 class="entry-title single-title">Non-Discrimination Policy</h1>


                        <p><b>Nondiscrimination Policy</b></p>
                        <p><b>We are committed to Mutual Respect and Inclusion</b></p>
                        <p>At the center of Health Care Travels philosophy is a strong commitment to making those that
                            share the same or similar healthcare background and those in the community better acquainted
                            and diverse by providing a platform for the propagation of meaningful and value-adding
                            experiences among people from every part of the United States of America.</p>
                        <p>Health Care Travels is dedicated to fostering an all-embracing community where people from
                            every background are always welcome and respected, irrespective of how far they are away
                            from home. Our platform is built on the principle of mutual respect and inclusion between
                            the travelers and the hosts. We believe these two principles are critical to making every
                            member of the Health Care Travels platform feel comfortable with each other irrespective of
                            religion, political affiliations, love life, sexual inclination, marital status,
                            nationality, or gender.</p>
                        <p>Health Care Travels understands that housing laws in some parts require Travelers and hosts
                            to abide by some rules. Health Care Travels will provide information about such areas where
                            outstanding regulations are enforced, and our nondiscrimination policy will be updated to
                            reflect such changes.</p>
                        <p>Health Care Travels has the mandate to support a harmonious relationship among every member
                            of our community to increase understanding, tolerance, and empathy across every social and
                            cultural divide. We have a mission to build a platform devoid of discrimination,
                            intolerance, and unlawful bias. This is why Health Care Travels employees, Travelers, and
                            Hosts are urged to read and act according to the provisions of the policy below to ensure
                            that every member of the community feels welcome in any part of the USA.</p>
                        <p><b>Mutual Respect</b></p>
                        <p>Mutual respect marks all our interactions and encounters. At Health Care Travels, we
                            appreciate the diversity of cultures and backgrounds in our community and expect Travelers
                            and hosts to be respectful during their engagements. While our primary objective is to make
                            short-term accommodation for healthcare professionals stress-free, we are also determined to
                            promote appreciation and understanding of the differences among members of our
                            community.</p>
                        <p><b>Inclusion</b></p>
                        <p>The Health Care Travels platform is open for use by every Traveler and host irrespective of
                            their race, religion, gender, and nationality. Our platform is devoid of racism, prejudice,
                            and bias. Hosts on our platform are mandated to follow extant legislation proscribing
                            discrimination based on attributes such as religion, race, nationality and other
                            stereotypical factors. We will also do our best to reduce any form of discrimination on our
                            platform to the barest minimum.</p>
                        <p><b>Important Guidance for Hosts</b></p>
                        <p>In general, we ensure that our nondiscrimination policies are in agreement with existing
                            local, state, and federal laws applicable to housing and public accommodation facilities.
                            Hosts should contact Health Care Travels customer support regarding questions about their
                            obligations to abide by our Nondiscrimination policy.</p>
                        <p><b>Race, Color, Nationality, Religion, Ethnicity, Gender Identity, Sexual Orientation, or
                                Marital Status</b></p>
                        <p>Health Care Traveler hosts may not</p>
                        <ul>
                            <li>Turn down a Traveler based on race, nationality, color, gender identity, sexual
                                orientation, religious affiliations, marital status, or ethnicity.
                            </li>
                            <li>Impose unfavorable terms or conditions on account of the Traveler’s racial roots, color,
                                nationality, ethnicity, gender identity, marital status, or religious identity.
                            </li>
                            <li>Make any statement or post any listings that show preference for/against or discourages
                                a Traveler based on their race, nationality, color, gender identity, sexual orientation,
                                religious affiliations, marital status, or ethnicity.
                            </li>
                        </ul>
                        <p><b>Gender</b></p>
                        <p>Health Care Traveler hosts may not</p>
                        <ul>
                            <li>Disapprove Travelers on account of gender except where the Host shares living spaces
                                such as the bathroom, common areas, and kitchen with the Traveler.
                            </li>
                            <li>Impose different terms and conditions on account of gender except where the Host and the
                                Traveler share living spaces.
                            </li>
                            <li>Make any pronouncement or post any listings that indicate a preference for/against or
                                discourages any Traveler based on gender except where the Traveler shares living spaces
                                with the host.
                            </li>
                        </ul>
                        <p>Health Care Travels hosts may</p>
                        <p>Rent a unit out to the Traveler of the same gender as the host where they both share living
                            spaces.</p>
                        <p><b>Disability</b></p>
                        <p>Health Care Travels hosts may not</p>
                        <p>Decline a Traveler on account of a perceived or actual disability.</p>
                        <ul>
                            <li>Impose different terms and conditions on account of the Traveler having a disability.
                            </li>
                            <li>Take a personal stand on whether a Traveler with a disability can use a unit rather than
                                allow the Traveler make the call.
                            </li>
                            <li>Seek to know of the existence or severity of a Traveler’s disability, or how the
                                Traveler manages the disability. In the event that a prospective Traveler raises his or
                                her disability, a host is encouraged to discuss the requirements of the Traveler to
                                ascertain whether the unit meets up to the Traveler’s requirements.
                            </li>
                            <li>Disallow or restrict the use of mobility equipment.</li>
                            <li>Charge more than the normal fees or rent for Travelers with disabilities.</li>
                            <li>Make any pronouncement or post any listings that indicate a preference for/against or
                                discourages any Traveler based on the Traveler’s disability.
                            </li>
                            <li>Decline communication with Travelers via convenient means like email for visually
                                impaired people using screen readers and relay operators used by people with audibility
                                issues.
                            </li>
                            <li>Decline to provide reasonable accommodations such as being rigid when Travelers with
                                disabilities seek modest adjustments to your house rules including bringing an
                                assistance animal which is necessitated by their disability or using the parking space
                                closest to the unit. In case of these types of requests, the host and Traveler should
                                discuss ways of making the use of the unit as comfortable as possible for the Traveler.
                            </li>
                        </ul>
                        <p>Health Care Travelers hosts may</p>
                        <p>Provide every information pertaining to the presence or absence of the unit’s accessibility
                            features, making it easier for Travelers with disabilities to determine by themselves
                            whether the unit meets their requirements.</p>
                        <p><b>Personal Preferences</b></p>
                        <p>Health Care Travels hosts may</p>
                        <ul>
                            <li>Excluding the instances stated above, Health Care Travels hosts may refuse to rent on
                                account of factors that are not prohibited by legislation. For, example, Health Care
                                Travels hosts may refuse rent to Travelers who smoke or who are bringing their pets,
                                except where prohibited by law.
                            </li>
                            <li>Require Travelers to follow food restrictions in the listing (for example, a host who
                                follows a ketogenic or vegetarian diet may mandate Travelers to follow their kitchen
                                rules.)
                            </li>
                            <li>This policy does not place any restrictions on the host to decline a Traveler over an
                                attribute that is not protected under civil rights laws or associated with them e.g., a
                                Health Care Travels host may decline a Traveler over smoking in the unit or restrict the
                                number of Travelers in a unit.
                            </li>
                        </ul>
                        <p><b>When hosts turn down Travelers</b></p>
                        <p>While hosts may have legitimate and articulate reasons for turning down prospective
                            Travelers, they should bear in mind that their actions may cause the Traveler to feel
                            unwelcome or excluded in our community. We enjoin hosts to do everything within their powers
                            to accommodate Travelers across all backgrounds. Hosts who have demonstrated a bias towards
                            a particular protected class (even when they have legitimate reasons for doing so), weaken
                            the cohesion holding our community together by creating a feeling of exclusion in potential
                            Travelers, and this can lead Health Care Travels to suspend such a host from using our
                            platform immediately.</p>
                        <p>When a host flouts our Nondiscrimination Policy</p>
                        <ul>
                            <li>If a host is found making statements divergent to Health Care Travelers’
                                Nondiscrimination Policy, we will request that the host remove the statements and show
                                proof of his or her understanding and willingness to abide by this policy and its
                                fundamental principles. At our discretion, Health Care Travels can take actions up to
                                the suspension of the host from the Health Care Travels platform.
                            </li>
                            <li>If the host rejects Travelers based on their association with a protected class or uses
                                language indicative of his or her actions being at variance with the provisions of this
                                policy, Health Care Travels will ensure that this policy is enforced, and this can
                                include the suspension of the host from our platform.
                            </li>
                        </ul>
                        <p>As we continue to grow, Health Care Travels will ensure that our policies and practices are
                            in congruence with our ideals and primary objectives: To ensure that every user is treated
                            with respect and hospitality while using the Health Care Travels platform. This is the lofty
                            standard we have set for ourselves, and we promise our users the best.</p>
